        <h2>¿Quiénes  somos?</h2>
  <p class="p-leyend2"><strong>Salud con vos</strong> es el portal del Programa Provincial de Salud Integral del Adolescente del Ministerio de Salud de la Provincia de Misiones que busca <strong>empoderar mediante la comunicación</strong> a los y las adolescentes.</p>
        <p class="p-leyend2">Nuestro  <strong>objetivo</strong>  es promover y garantizar los derechos de salud de los y las adolescentes, entendiendo la salud desde una perspectiva  integral que contemple la salud física, la psicológica y emocional.</p>
       
   <img width="1010" height="468" src="{{ asset('plantillas/simplicity/wp-content/uploads/2013/08/063.jpg')}}" class="attachment-full wp-post-image" alt="06">

   
       <p class="p-leyend2">Nuestras <strong>funciones</strong> se centran en el armado de redes de atención y contención de los y las adolescentes de la Provincia.</p>
        <div class="list circle animate_list">            
            <ul>
                <li style="opacity: 1; top: 0px;">Capacitamos y brindamos asesoramiento técnico a equipos de salud en la atención integral de los y las adolescentes.</li>
                <li style="opacity: 1; top: 0px;">Articulamos estrategias con otros Ministerios e Instituciones comprometidas con la temática.</li>
                <li style="opacity: 1; top: 0px;">Apoyamos la creación y/o el fortalecimiento de servicios de atención de la salud integral del adolescente en cada jurisdicción.</li>
                <li style="opacity: 1; top: 0px;">Brindamos capacitaciones y dinámicas de sensibilización en escuelas, centros de salud y Centros Integradores Comunitarios.</li>
                <li style="opacity: 1; top: 0px;">Generamos espacios de comunicación y participación para hacer efectivo el acceso a la información.</li>
                <li style="opacity: 1; top: 0px;">Realizamos estudios que nos permiten analizar y monitorear problemáticas sanitarias y el alcance de las acciones implementadas.</li>         
            </ul>
